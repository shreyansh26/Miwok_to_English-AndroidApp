public class Phrase {

    private String defaultString;
    private String miwokString;

    public Phrase(String s1, String s2) {
        defaultString = s1;
        miwokString = s2;

    }
    public String getdefaultString() {
        return defaultString;
    }

    public String getmiwokString() {
        return miwokString;
    }

}
